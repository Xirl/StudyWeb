package com.sx.bean;

public class Tags {
	private Integer id; 
	private String status;
	private Integer typeId; 
	private String type;
	private Integer totheme;
	private Integer tagsUser;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public Integer getTypeId() {
		return typeId;
	}
	public void setTypeId(Integer typeId) {
		this.typeId = typeId;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Integer getTagsUser() {
		return tagsUser;
	}
	public void setTagsUser(Integer tagsUser) {
		this.tagsUser = tagsUser;
	}
	public Integer getTotheme() {
		return totheme;
	}
	public void setTotheme(Integer totheme) {
		this.totheme = totheme;
	}
	
}
